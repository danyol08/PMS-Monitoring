// Utility functions for consistent styling
export const getCardStyles = (variant: 'default' | 'elevated' | 'outlined' = 'default') => {
  const base = 'bg-white rounded-xl border'
  
  switch (variant) {
    case 'elevated':
      return `${base} shadow-lg border-gray-200`
    case 'outlined':
      return `${base} border-gray-300 shadow-sm`
    default:
      return `${base} border-gray-200 shadow-sm`
  }
}

export const getButtonStyles = (variant: 'primary' | 'secondary' | 'danger' | 'ghost' = 'primary', size: 'sm' | 'md' | 'lg' = 'md') => {
  const base = 'inline-flex items-center justify-center font-medium rounded-lg transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-offset-2 disabled:opacity-50 disabled:cursor-not-allowed'
  
  const variants = {
    primary: 'bg-blue-600 text-white hover:bg-blue-700 focus:ring-blue-500 shadow-sm hover:shadow-md',
    secondary: 'bg-gray-100 text-gray-900 hover:bg-gray-200 focus:ring-gray-500',
    danger: 'bg-red-600 text-white hover:bg-red-700 focus:ring-red-500 shadow-sm hover:shadow-md',
    ghost: 'text-gray-600 hover:text-gray-900 hover:bg-gray-100 focus:ring-gray-500'
  }
  
  const sizes = {
    sm: 'px-3 py-1.5 text-sm',
    md: 'px-4 py-2 text-sm',
    lg: 'px-6 py-3 text-base'
  }
  
  return `${base} ${variants[variant]} ${sizes[size]}`
}

export const getInputStyles = (error = false) => {
  const base = 'block w-full rounded-lg border px-3 py-2 text-sm placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-offset-0 transition-colors duration-200'
  
  if (error) {
    return `${base} border-red-300 focus:border-red-500 focus:ring-red-500`
  }
  
  return `${base} border-gray-300 focus:border-blue-500 focus:ring-blue-500`
}

export const getStatusStyles = (status: 'success' | 'warning' | 'error' | 'info') => {
  const styles = {
    success: 'bg-green-50 text-green-800 border-green-200',
    warning: 'bg-yellow-50 text-yellow-800 border-yellow-200',
    error: 'bg-red-50 text-red-800 border-red-200',
    info: 'bg-blue-50 text-blue-800 border-blue-200'
  }
  
  return `inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium border ${styles[status]}`
}

